export function getUseCaseById(id: string): UseCase | undefined {
  return useCases.find(uc => uc.id === id)
}

export function getUseCasesByDivision(division: string): UseCase[] {
  return useCases.filter(uc => uc.division === division)
}

export function getUseCasesByStatus(status: UseCase['status']): UseCase[] {
  return useCases.filter(uc => uc.status === status)
}

export function getUseCasesByPageType(pageType: UseCase['pageType']): UseCase[] {
  return useCases.filter(uc => uc.pageType === pageType)
}

export function searchUseCases(query: string): UseCase[] {
  const lowerQuery = query.toLowerCase()
  return useCases.filter(uc =>
    uc.serviceName.toLowerCase().includes(lowerQuery) ||
    uc.useCase.toLowerCase().includes(lowerQuery) ||
    uc.url.toLowerCase().includes(lowerQuery) ||
    uc.description?.toLowerCase().includes(lowerQuery) ||
    uc.tags?.some(tag => tag.toLowerCase().includes(lowerQuery))
  )
}
